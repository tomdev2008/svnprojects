package com.tencent.weibo.dao.impl;

public class SimpleUser {

	private int exp;
	private int fansnum;
	private String head;
	private String https_head;
	private int idolnum;
	private boolean isidol;
	private int isrealname;
	private int isvip;
	private int level;
	private String name;
	private String nick;
	private String openid;
	private int sex;

	public int getExp() {
		return exp;
	}

	public void setExp(int exp) {
		this.exp = exp;
	}

	public int getFansnum() {
		return fansnum;
	}

	public void setFansnum(int fansnum) {
		this.fansnum = fansnum;
	}

	public String getHead() {
		return head;
	}

	public void setHead(String head) {
		this.head = head;
	}

	public String getHttps_head() {
		return https_head;
	}

	public void setHttps_head(String https_head) {
		this.https_head = https_head;
	}

	public int getIdolnum() {
		return idolnum;
	}

	public void setIdolnum(int idolnum) {
		this.idolnum = idolnum;
	}

	public boolean isIsidol() {
		return isidol;
	}

	public void setIsidol(boolean isidol) {
		this.isidol = isidol;
	}

	public int getIsrealname() {
		return isrealname;
	}

	public void setIsrealname(int isrealname) {
		this.isrealname = isrealname;
	}

	public int getIsvip() {
		return isvip;
	}

	public void setIsvip(int isvip) {
		this.isvip = isvip;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNick() {
		return nick;
	}

	public void setNick(String nick) {
		this.nick = nick;
	}

	public String getOpenid() {
		return openid;
	}

	public void setOpenid(String openid) {
		this.openid = openid;
	}

	public int getSex() {
		return sex;
	}

	public void setSex(int sex) {
		this.sex = sex;
	}

}
