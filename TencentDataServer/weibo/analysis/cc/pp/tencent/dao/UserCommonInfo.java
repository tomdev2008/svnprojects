package cc.pp.tencent.dao;

public class UserCommonInfo {

	private String name;
	private int fansnum;
	private int idolnum;
	private int tweetnum;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getFansnum() {
		return fansnum;
	}

	public void setFansnum(int fansnum) {
		this.fansnum = fansnum;
	}

	public int getIdolnum() {
		return idolnum;
	}

	public void setIdolnum(int idolnum) {
		this.idolnum = idolnum;
	}

	public int getTweetnum() {
		return tweetnum;
	}

	public void setTweetnum(int tweetnum) {
		this.tweetnum = tweetnum;
	}

}
