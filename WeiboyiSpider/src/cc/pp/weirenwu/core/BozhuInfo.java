package cc.pp.weirenwu.core;

public class BozhuInfo {

	private String nickname;
	private String username;
	private String influence;
	private String cate;
	private String canliprice;
	private String fanscount;
	private String jiedanratio;

	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}
	public String getInfluence() {
		return influence;
	}
	public void setInfluence(String influence) {
		this.influence = influence;
	}
	public String getCate() {
		return cate;
	}
	public void setCate(String cate) {
		this.cate = cate;
	}
	public String getCanliprice() {
		return canliprice;
	}
	public void setCanliprice(String canliprice) {
		this.canliprice = canliprice;
	}
	public String getFanscount() {
		return fanscount;
	}
	public void setFanscount(String fanscount) {
		this.fanscount = fanscount;
	}
	public String getJiedanratio() {
		return jiedanratio;
	}
	public void setJiedanratio(String jiedanratio) {
		this.jiedanratio = jiedanratio;
	}


}
